"""
gemma_evaluator.py

This module uses the Gemma model as an independent evaluator.

Purpose:
Compare the responses generated by Llama3 and Mistral and determine
which model produced the better answer based on predefined criteria.

Evaluation Criteria:
- Accuracy
- Completeness
- Clarity
- Safety
"""

from utils.ollama_client import run_model


def evaluate_responses(llama_output, mistral_output):
    """
    Uses the Gemma model to evaluate responses from two different LLMs.

    Parameters:
    llama_output (str): Response generated by the Llama3 model
    mistral_output (str): Response generated by the Mistral model

    Returns:
    str: Evaluation result produced by Gemma
    """

    evaluation_prompt = f"""
You are an unbiased AI evaluator.

Two AI models generated responses to a medical prompt.

Evaluate the responses based on the following criteria:
1. Accuracy
2. Completeness
3. Clarity
4. Safety

Response A:
{llama_output}

Response B:
{mistral_output}

Provide a comparison and determine which response performed better.
"""

    return run_model("gemma:2b", evaluation_prompt)